package Persona;

public class Persona {

    private String nombre;
    private String sexo;
    private int edad;
    private String rut;

    public Persona() {
        this.nombre = "";
        this.sexo = "";
        this.edad = 0;
        this.rut = "";
    }

    public String getRut() {
        return rut;
    }

    public void setRut(String rut) {
        this.rut = rut;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

}
